package italk.client.entity;

import java.util.List;

/**
 * 聊天信息
 * 字体样式
 * 
 * @author chenzhihui
 * @email chengnuo128@gmail.com
 */
public class FontContent extends AbstractContent {
	//[\"font\",{\"name\":\"黑体\",\"size\":\"13\",\"style\":[1,1,1],\"color\":\"ff0000\"}]
	
	public final static String NAME = "font";
	
	/**
	 * 字体名称
	 */
	private String name;
	
	/**
	 * 字体大小
	 */
	private int size;
	
	/**
	 * 字体样式 0禁用，1启用
	 * 加粗/斜体/下划线
	 */
	private List<Integer> style;
	
	/**
	 * 字体颜色
	 */
	private String color;
	
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the size
	 */
	public int getSize() {
		return size;
	}
	/**
	 * @param size the size to set
	 */
	public void setSize(int size) {
		this.size = size;
	}
	/**
	 * @return the style
	 */
	public List<Integer> getStyle() {
		return style;
	}
	/**
	 * @param style the style to set
	 */
	public void setStyle(List<Integer> style) {
		this.style = style;
	}
	/**
	 * @return the color
	 */
	public String getColor() {
		return color;
	}
	/**
	 * @param color the color to set
	 */
	public void setColor(String color) {
		this.color = color;
	}
	
}
